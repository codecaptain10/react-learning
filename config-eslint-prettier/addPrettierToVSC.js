//How can I add a prettier to Visual Studio Code ?
/*
1) Install Prettier-Code Formatter

2) Settings -> Editor:Default Formatter -> Chose "Prettier - Code Formatter"

3) Settings -> Arrow Parrens -> Chose to avoid

4) Settings -> Prettier JSX -> Chose two Settings
    1. Prettier Jsx Bracket Same Line
    2. PrettierL Jsx Single Quote

5) Settings -> Prettier Semi -> Chose this setting

6) Settings -> Prettier Use Tabs -> Chose this setting

7) Go to JSON Settings ->  
    ...
    "[html]": {
        "editor.defaultFormatter": "vscode.html-language-features"
    },
        
    "emmet.syntaxProfiles": {
        "html": {
        "inline_break": 2
        }
    },
    "emmet.variables": {
        "lang": "pl"
    }
    ...

*/
